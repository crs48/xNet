#!/usr/bin/env node
/**
 * xNet Cloud — scaffold a control-plane .env file for one environment.
 *
 *   node scripts/cloud-init-env.mjs development        # → apps/cloud/.env.development
 *   node scripts/cloud-init-env.mjs staging
 *   node scripts/cloud-init-env.mjs production
 *
 * Non-secret config is pre-filled per environment, the random control-plane
 * secrets are GENERATED for you, and every external credential is left as a
 * CHANGEME_<KEY> placeholder with a comment saying where to get it. Then run
 * the doctor to see what's left:
 *
 *   node scripts/cloud-env-doctor.mjs apps/cloud/.env.staging
 *
 * Flags:  --force overwrite an existing file · --out <path> write elsewhere
 * The generated file is git-ignored — never commit it.
 */

import { randomBytes } from 'node:crypto'
import { existsSync, mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { ENVIRONMENTS, GENERATE, FILL, VARS, rawValue } from './cloud-env-schema.mjs'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const args = process.argv.slice(2)
const env = args.find((a) => !a.startsWith('--')) ?? 'development'
const force = args.includes('--force')
const outIdx = args.indexOf('--out')
const outPath =
  outIdx >= 0 ? resolve(args[outIdx + 1]) : resolve(repoRoot, `apps/cloud/.env.${env}`)

if (!ENVIRONMENTS.includes(env)) {
  console.error(`Unknown environment "${env}". Use one of: ${ENVIRONMENTS.join(', ')}`)
  process.exit(1)
}
if (existsSync(outPath) && !force) {
  console.error(`Refusing to overwrite ${outPath} (pass --force to replace it).`)
  process.exit(1)
}

const hex = () => randomBytes(32).toString('hex')

const lines = [
  `# xNet Cloud control plane — ${env}`,
  `# Generated by scripts/cloud-init-env.mjs. SECRETS ARE REAL — never commit this file.`,
  `# Fill every CHANGEME_* value, then: node scripts/cloud-env-doctor.mjs ${outPath.replace(repoRoot + '/', '')}`,
  ''
]

let lastGroup = null
let generated = 0
let toFill = 0
for (const spec of VARS) {
  const raw = rawValue(spec, env)
  if (spec.group !== lastGroup) {
    lines.push(`# ── ${spec.group} ${'─'.repeat(Math.max(2, 70 - spec.group.length))}`)
    lastGroup = spec.group
  }
  let value
  if (raw === GENERATE) {
    value = hex()
    generated++
  } else if (raw === FILL) {
    value = `CHANGEME_${spec.key}`
    toFill++
  } else {
    value = raw
  }
  const tag = spec.secret ? ' (secret)' : ''
  lines.push(`# ${spec.where}${tag}`)
  lines.push(`${spec.key}=${value}`)
}
lines.push('')

mkdirSync(dirname(outPath), { recursive: true })
writeFileSync(outPath, lines.join('\n'), { mode: 0o600 })
console.log(`✓ Wrote ${outPath}`)
console.log(`  • ${generated} secret(s) generated for you`)
console.log(`  • ${toFill} value(s) left as CHANGEME_* to fill in`)
console.log(`\nNext: open the file, fill the CHANGEME_* values (see docs/cloud/SETUP.md),`)
console.log(`then run:  node scripts/cloud-env-doctor.mjs ${outPath.replace(repoRoot + '/', '')}`)
